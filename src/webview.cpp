std::optional<void*> Webview::window() {
    auto result = webview_impl->window();
    return result.has_value() ? std::optional<void*>(result.value()) : std::nullopt;
}

// Windows: ICoreWebView2*, Linux: WebKitWebView*, macOS: WKWebView*
std::optional<void*> Webview::widget() {
#if defined(_WIN32)
    // widget() from the library returns HWND; use get_webview() for ICoreWebView2*.
    auto* engine_ptr = static_cast<webview::detail::win32_edge_engine*>(webview_impl.get());
    if (!engine_ptr) return std::nullopt;
    auto wv = engine_ptr->get_webview();
    return wv ? std::optional<void*>(wv) : std::nullopt;
#else
    auto result = webview_impl->widget();
    return result.has_value() ? std::optional<void*>(result.value()) : std::nullopt;
#endif
}

#if defined(_WIN32)
std::optional<void*> Webview::get_controller() {
    auto result = webview_impl->browser_controller();
    return result.has_value() ? std::optional<void*>(result.value()) : std::nullopt;
}

void Webview::register_navigation_handler(std::function<bool(const std::string&)> callback) {
    auto* engine_ptr = static_cast<webview::detail::win32_edge_engine*>(webview_impl.get());
    if (engine_ptr)
        engine_ptr->set_navigation_callback(callback);
}
#endif
